function changebackground() {
    let magicNumber = 1; // Définir magicNumber sur 1

    if (magicNumber === 1) {
        paintYellowRectangle();
    }

    magicNumber = 2; // Passer à 2
    if (magicNumber === 2) {
        paintGreenRectangle();
    }

    magicNumber = 3; // Passer à 3
    if (magicNumber === 3) {
        paintRedRectangle();
    }
}

changebackground();
